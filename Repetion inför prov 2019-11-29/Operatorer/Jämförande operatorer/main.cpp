// Jämförande operatorer
#include <iostream>
using namespace std;
int main() {
	// Används i samband med villkorssatser
	// och loopar (if, for, while)
	
	// jämförelseoperatorn
	cout << (1==1) << endl;  // lika
    cout << (1!=1) << endl;  // inte lika
    cout << (1<1) << endl;  // Mindre än
    cout << (1<=1) << endl;  // Mindre än eller lika med
    cout << (1>1) << endl;  // Större än
    cout << (1>=1) << endl;  // Större än eller lika med
	return 0;
}
